void grayscale(int height, int width, RGBTRIPLE image[height][width])
{
    // in order to get  the grey color , the red,green and blue should be equal
    // higher values lighter shades
    // lower vlues darker shades
	// the top value is height-1 and width-1

   int avg = 0;
     for (int i = 0; i < height; i++)
    {
        for (int j = 0; j < width; j++)
        {
            //calculate the average pixel value
            avg = round((image[i][j].rgbtRed + image[i][j].rgbtGreen +  image[i][j].rgbtBlue)/(float)3);
            //set each color value to the average value
            image[i][j].rgbtRed = avg;
            image[i][j].rgbtGreen = avg;
            image[i][j].rgbtBlue = avg;

        }
    }

    return;
}


